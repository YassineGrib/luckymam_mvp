/// Helper to get milestones by phase
List<Milestone> getMilestonesByPhase(Phase phase) {
  return allMilestones.where((m) => m.phase == phase).toList();
}

/// Helper to get milestones by category
List<Milestone> getMilestonesByCategory(MilestoneCategory category) {
  return allMilestones.where((m) => m.category == category).toList();
}

/// Get milestone by ID
Milestone? getMilestoneById(String id) {
  try {
    return allMilestones.firstWhere((m) => m.id == id);
  } catch (_) {
    return null;
  }
}
